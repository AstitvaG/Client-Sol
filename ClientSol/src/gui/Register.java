    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // Submit Button
        try{
        name = n.getText();
        if(g1.isSelected()){gender=g1.getText();}
        else if(g2.isSelected()){gender=g2.getText();}
        else{gender=g3.getText();}
        dt = d.getDate();
        country = (String)c.getSelectedItem();
        address = a.getText();
        int count_languages=0;
        languages="";
        if(l1.isSelected()){languages+=l1.getText()+",";count_languages++;}
        if(l2.isSelected()){languages+=l2.getText()+",";count_languages++;}
        if(l3.isSelected()){languages+=l3.getText()+",";count_languages++;}
        if(l4.isSelected()){languages+=l4.getText()+",";count_languages++;}
        if(l5.isSelected()){languages+=l5.getText()+",";count_languages++;}
        if(l6.isSelected()){languages+=l6.getText()+",";count_languages++;}
        if( !languages.equals(""))
        {
            languages = languages.substring(0, languages.length()-1);
        }
        if( !name.equals("") && !country.equals("--Select Country--") && !address.equals(""))
        {
            nd.setText(dt.toString());
            nn.setText(name);
            na.setText(address);
            nl.setText("("+count_languages+")"+languages);
            ng.setText(gender);
            nc.setText(country);
            Container c11 = getContentPane();
            CardLayout cd = (CardLayout)c11.getLayout();
            cd.next(c11);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Incomplete Details !");
        }
        }
        catch(Exception ex){JOptionPane.showMessageDialog(this, "Date not correct (format: dd/mm/yyyy) !");}
    }//GEN-LAST:event_button1ActionPerformed

