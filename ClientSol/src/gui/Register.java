    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        //Done Button:
        try
        {
                java.sql.Date dob = new java.sql.Date(dt.getTime());
                db.DBConnect.AddClient.setString(1, name);
                db.DBConnect.AddClient.setString(2, gender);
                db.DBConnect.AddClient.setDate(3, dob);
                db.DBConnect.AddClient.setString(4, country);
                db.DBConnect.AddClient.setString(5, address);
                db.DBConnect.AddClient.setString(6, languages);
                db.DBConnect.AddClient.executeUpdate();
                //db.DBConnect.st.executeUpdate("insert into client_info (name,gender,dob,country,address,languages) values ('"+name+"','"+gender+"','"+dob+"','"+country+"','"+address+"','"+languages+"')");
                JOptionPane.showMessageDialog(this, "Client added successfully.");
                dispose();
                new Register().setVisible(true);
        }
        catch(Exception ex)
        {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_button3ActionPerformed

